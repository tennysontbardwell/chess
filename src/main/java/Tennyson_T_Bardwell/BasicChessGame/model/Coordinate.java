package Tennyson_T_Bardwell.BasicChessGame.model;

/** Holds a location on the board, and is the standard way to reference a
 * location on the board
 * 
 * @author tbTennyson */
public class Coordinate {
	final public int x;
	final public int y;

	public Coordinate(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
